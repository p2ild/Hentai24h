public class Catalog {
    private String urlCatalog;
    private String nameCatalog;

    public Catalog(String urlCatalog, String nameCatalog) {
        this.urlCatalog = urlCatalog;
        this.nameCatalog = nameCatalog;
    }

    public String getUrlCatalog() {
        return urlCatalog;
    }

    public String getNameCatalog() {
        return nameCatalog;
    }
}
